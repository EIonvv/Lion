#include "math.h"

double linux_branch::math::operations::add(double a, double b) { return a + b; }
double linux_branch::math::operations::subtract(double a, double b) {
  return a - b;
}
double linux_branch::math::operations::multiply(double a, double b) {
  return a * b;
}

double linux_branch::math::operations::divide(double a, double b) {
  return a / b;
}
